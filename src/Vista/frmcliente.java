  private void listarClientes() {
        
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("DNI");
        modelo.addColumn("Nombres");
        modelo.addColumn("Apellidos");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Correo");

        
        Controlador.ClienteDAO clienteDao = new Controlador.ClienteDAO();
        
        // 3. Obtenemos la lista desde el DAO y la recorremos
        // Nota: Tu ClienteDAO debe tener un método llamado 'listar()' o 'listarClientes()' que devuelva un List
        java.util.List<Modelo.Cliente> lista = clienteDao.listar(); 
        
        if (lista != null) {
            for (Modelo.Cliente c : lista) {
                Object[] fila = new Object[5];
                fila[0] = c.getDni();
                fila[1] = c.getNombres();
                fila[2] = c.getApellidos();
                fila[3] = c.getTelefono();
                fila[4] = c.getCorreo();
                modelo.addRow(fila);
            }
        }
        
        tblClientes.setModel(modelo);
    }

   
