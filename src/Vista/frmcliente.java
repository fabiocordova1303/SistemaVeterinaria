    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
String dniBuscar = txtDni.getText().trim();
if (dniBuscar.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingrese un DNI para buscar.");
    return;
}

Cliente = clienteDao.buscarPorDni(dniBuscar);

if (Cliente != null) {
    txtNombres.setText(Cliente.getNombres());
    txtApellidos.setText(Cliente.getApellidos());
    txtTelefono.setText(Cliente.getTelefono());
    txtCorreo.setText(Cliente.getCorreo());
} else {
    javax.swing.JOptionPane.showMessageDialog(this, "No se encontró ningún cliente con ese DNI.");
}
    }//GEN-LAST:event_btnBuscarActionPerformed

